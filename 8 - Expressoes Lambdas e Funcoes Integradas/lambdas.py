# Lambdas

# Conhecidas por Expressões Lambdas, ou simplesmente Lambdas, são funções sem nome, ou seja, funções anônimas.

# Função em Python

def soma(a, b): # exemplo 1
    return a + b

def funcao(x): # exemplo 2
    return 3 * x + 1

# print(funcao(4)) # ->

# Exemplo de Expressão Lambda
lambda x: 3 * x + 1

# E como utilizar expressões lambdas?
calc = lambda x: 3 * x + 1
# print(calc(4)) -> 13