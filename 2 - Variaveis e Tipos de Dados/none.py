# Tipo None

# None em python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio. Porém,
# falar que é um tipo sem tipo é mais apropriado.

numeros =  None

print(numeros)
print(type(numeros))

# Certo: None
# Errado: none

# Quando utilizamos?
# - Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes
# de recebermos um valor final.

# Obs: O tipo None em python sempre será considerado False.